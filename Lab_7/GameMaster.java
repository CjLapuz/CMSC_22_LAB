    public void results(){
        if (Integer.parseInt(dieded.getText()) == 1){
                JOptionPane.showMessageDialog(null, "VICTORY!!!", "!GL FURTHER!", 2);
                hero.levelUp(1);
                JOptionPane.showMessageDialog(null, "YOU ARE NOW LEVEL " +hero.getLevel(), "LEVEL UP!", 2);
                heroName.setText(getHeroName());
                hero.updateHealth();
                hero.updateMana();
                setHeroHealth(hero.getHp());
                setHeroMana(hero.getMP());
                disabled.setText("0");
                if (hero.getLevel() % 10 != 0){
                    generateMonster();
                } else {
                    JOptionPane.showMessageDialog(null, "BOSS BATTLE", "!!!WARNING!!!", 0);
                    generateBoss();
                }
            } else if (Integer.parseInt(dieded.getText()) == -1){
                JOptionPane.showMessageDialog(null, "Defeated!!", "!GL NEXT TIME!", 2);
                main.setSelectedIndex(3);
            }
    }
