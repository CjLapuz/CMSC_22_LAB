    private void journeyMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_journeyMouseClicked
        if (job.getText().equals("") || name.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Choose your Name / Class", "Error", 0);
        } else {
        if(rpg.confirm()){
            switch (Integer.parseInt(job.getText())){
                case 1:
                    hero = new Swordsman(name.getText(), 1);
                    break;
                case 2:
                    hero = new Archer(name.getText(), 1);
                    break;
                case 3:
                    hero = new Sorcerer(name.getText(), 1);
                    break;
                case 4:
                    hero = new Assassin(name.getText(), 1);
                    break;
                default:
            }
            setHeroHealth(hero.getHp());
            setHeroMana(hero.getMP());
            heroName.setText(getHeroName());
            main.setSelectedIndex(2);
                monster = new UnremarkableSlime(hero.getLevel());
                event.setIcon(new javax.swing.ImageIcon(getClass().getResource("/RPG/slimeHome.png")));
                roar.setIcon(new javax.swing.ImageIcon(getClass().getResource("/RPG/slime.gif")));
                monsName.setText(getMonsName());
                setMonsterHealth(monster.getHp());
                rpg.playerEvent();
        }
        }
    }//GEN-LAST:event_journeyMouseClicked

